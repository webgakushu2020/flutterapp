①関数って？
処理をひとまとめにした箱！
function（ファンクション）とも呼ばれる

＊ポイント＊
・実は…今までにたくさん使ってきた「print()」も関数！
・すべてを囲っているvoid main()〜も関数！！
→プログラムが実行されたとき、自動でmain関数が実行されるようになっている

----------------------------------

②関数を書いてみよう！

関数は
・作る
・実行する

の2段階が必要！

・あいさつプログラムを作ろう

【コード】

void main() {
  void greet() {
    print('こんにちは');
  }
}

【実行結果】
(なにもでない)


！実行させるために、関数の名前()を追記しよう！


【コード】
void main() {
  void greet() {
    print('こんにちは');
  }

  greet();
}

【実行結果】
